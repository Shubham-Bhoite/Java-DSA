public class Switch {
    public static void main(String args[]){
        char ch = 'b';
        switch (ch){
            case 'a': System.out.println("VadaPav");
            break;
            case 'b' : System.out.println("Burger");
            break;
            case 'c' : System.out.println("Banana");
            break;
            default : System.out.println("Enter valid character ");

        }
    }
}
